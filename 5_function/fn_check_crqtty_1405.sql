SET DEFINE OFF;
CREATE OR REPLACE FUNCTION fn_check_crqtty_1405(P_CPHYSAGREEID VARCHAR2) 
RETURN NUMBER IS
  V_RESULT NUMBER;
BEGIN
     SELECT CR.QTTY INTO V_RESULT
     FROM CRPHYSAGREE CR
     WHERE CR.CRPHYSAGREEID = P_CPHYSAGREEID;
     RETURN V_RESULT;
     EXCEPTION WHEN OTHERS THEN RETURN 0;
END;
/
