CREATE OR REPLACE PROCEDURE pr_insert_signature(V_CUSTID varchar2,V_SIGNATURE CLOB,V_VALDATE Varchar2,V_EXPDATE Varchar2) IS
    BEGIN
       INSERT INTO cfsign(AUTOID,CUSTID,SIGNATURE,VALDATE,EXPDATE)
       VALUES(SEQ_CFSIGN.NEXTVAL,V_CUSTID,V_SIGNATURE,TO_DATE(V_VALDATE,'DD/MM/YYYY'),TO_DATE(V_EXPDATE,'DD/MM/RRRR'));
    END ;
 
 
 
 
 
 
 
 
 
 
 
 
/
