SET DEFINE OFF;
CREATE OR REPLACE PROCEDURE update_cfauth
(
    V_CUSTID varchar2, V_SIGNATURE varchar2, V_VALDATE varchar2, V_EXPDATE varchar2,V_AUTOID NUMBER,
    v_LINKAUTH varchar2, v_FULLNAME varchar2, v_ADDRESS varchar2, v_TELEPHONE varchar2, v_LICENSENO varchar2,
    v_BANKNAME varchar2, v_LNPLACE varchar2, v_LNIDDATE varchar2,v_EMAIL varchar2
) IS
 LONGLITERAL VARCHAR2(32767);
    BEGIN
        LONGLITERAL:=V_SIGNATURE;

        UPDATE CFAUTH  SET SIGNATURE = LONGLITERAL, VALDATE = TO_DATE(V_VALDATE,'DD/MM/RRRR'), EXPDATE = TO_DATE(V_EXPDATE,'DD/MM/RRRR'),
           LINKAUTH = v_LINKAUTH, FULLNAME = v_FULLNAME, ADDRESS = v_ADDRESS, TELEPHONE = v_TELEPHONE, LICENSENO = v_LICENSENO,
           BANKNAME = v_BANKNAME, LNPLACE = v_LNPLACE, LNIDDATE = TO_DATE(v_LNIDDATE,'DD/MM/RRRR'), EMAIL=v_EMAIL
        WHERE AUTOID = V_AUTOID;

    END ;
/
