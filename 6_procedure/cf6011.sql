    FROM (
        SELECT CF.FULLNAME, CF.AMCID FROM CFMAST CF WHERE CF.STATUS NOT IN ('C') AND CF.CUSTODYCD <> V_CUSTODYCD AND CF.AMCID = V_AMCID GROUP BY CF.FULLNAME, CF.AMCID
    );
EXCEPTION
          WHEN NO_DATA_FOUND THEN
            V_LISTFUND:= NULL;

END;
