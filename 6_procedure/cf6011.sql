OPEN PV_REFCURSOR FOR
    SELECT
           'CF6011' MASTERID,
           P_REPORTNO REPORTNO,
           TO_CHAR(V_FROMDATE,'DD') DAYSTR,
           TO_CHAR(V_FROMDATE,'MM') MONTHSTR,
