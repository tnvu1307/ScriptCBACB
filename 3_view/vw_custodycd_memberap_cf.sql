SELECT BR.CUSTID FILTERCD,BR.REFID VALUE, BR.REFID VALUECD,M.SHORTNAME ||'-'|| M.FULLNAME DISPLAY,M.SHORTNAME ||'-'|| M.FULLNAME EN_DISPLAY, M.FULLNAME DESCRIPTION, m.englishname EN_DESCRIPTION
from CFLNKAP BR,FAMEMBERS M
where BR.REFID = M.AUTOID
