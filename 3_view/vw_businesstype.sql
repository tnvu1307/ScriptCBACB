SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW VW_BUSINESSTYPE
(MODCODE, OBJNAME, ACTYPE, TYPENAME)
AS 
SELECT 'DD' MODCODE, 'DD.CITYPE' OBJNAME, ACTYPE, TO_CHAR(TYPENAME) TYPENAME FROM DDTYPE
UNION ALL
SELECT 'SE' MODCODE, 'SE.SETYPE' OBJNAME, ACTYPE, TO_CHAR(DESCRIPTION) TYPENAME FROM SETYPE
UNION ALL
SELECT 'OD' MODCODE, 'OD.ODTYPE' OBJNAME, ACTYPE, TO_CHAR(TYPENAME) TYPENAME FROM ODTYPE
/
