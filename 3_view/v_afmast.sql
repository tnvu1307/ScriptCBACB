SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW V_AFMAST
(ACTYPE, CUSTID, ACCTNO, AFTYPE, TRADEFLOOR, 
 TRADETELEPHONE, TRADEONLINE, PIN, TRADEPHONE, BANKACCTNO, 
 BANKNAME, SWIFTCODE, EMAIL, ADDRESS, FAX, 
 CIACCTNO, LASTDATE, STATUS, ADVANCELINE, BRATIO, 
 TERMOFUSE, DESCRIPTION)
AS 
SELECT AF.ACTYPE, AF.CUSTID, AF.ACCTNO, AF.AFTYPE , A1.CDCONTENT TRADEFLOOR, A2.CDCONTENT TRADETELEPHONE,
    A3.CDCONTENT TRADEONLINE, cf.PIN, cf.mobilesms TRADEPHONE,
    AF.BANKACCTNO, A6.CDCONTENT BANKNAME, cf.SWIFTCODE, cf.EMAIL, cf.ADDRESS, cf.FAX,
    af.acctno CIACCTNO, AF.LASTDATE, A8.CDCONTENT STATUS, ADVANCELINE, BRATIO, A9.CDCONTENT TERMOFUSE, af.DESCRIPTION
FROM CFMAST CF, AFMAST AF, ALLCODE A1, ALLCODE A2, ALLCODE A3, ALLCODE A6,
    ALLCODE A8, ALLCODE A9
WHERE A1.CDTYPE = 'SY' AND A1.CDNAME = 'YESNO' AND A1.CDVAL= cf.TRADEFLOOR
    AND A2.CDTYPE = 'SY' AND A2.CDNAME = 'YESNO' AND A2.CDVAL= cf.TRADETELEPHONE
    AND A3.CDTYPE = 'SY' AND A3.CDNAME = 'YESNO' AND A3.CDVAL= cf.TRADEONLINE
    AND A6.CDTYPE = 'CF' AND A6.CDNAME = 'BANKNAME' AND A6.CDVAL = af.BANKNAME
    AND A8.CDTYPE = 'CF' AND A8.CDNAME = 'STATUS' AND A8.CDVAL = af.STATUS
    AND A9.CDTYPE = 'CF' AND A9.CDNAME = 'TERMOFUSE' AND A9.CDVAL = af.TERMOFUSE
/
