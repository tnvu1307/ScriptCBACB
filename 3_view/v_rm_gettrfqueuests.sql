SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW V_RM_GETTRFQUEUESTS
(VERSION, VERSIONLOCAL, TRFCODE, TXDATE, BANKCODE, 
 CORD, UNHOLD)
AS 
SELECT CRT.VERSION,CRT.VERSIONLOCAL,CRT.TRFCODE,CRT.TXDATE,
CRT.REFBANK BANKCODE,CRA.REFDORC CORD,CRA.REFUNHOLD UNHOLD
FROM CRBTRFLOG CRT,CRBDEFACCT CRA
WHERE CRT.REFBANK=CRA.REFBANK AND CRT.TRFCODE=CRA.TRFCODE
AND CRT.VERSION IS NOT NULL AND CRT.VERSIONLOCAL IS NOT NULL
AND CRT.VERSIONLOCAL <>'---'
AND cspks_rmproc.is_number(CRA.MSGID)=1
AND (CRT.STATUS IN ('S','H') or (CRT.STATUS = 'E'  and txdate = getcurrdate))
AND CRT.ERRSTS <> 'E'
ORDER BY CRT.TXDATE DESC,CRT.VERSION ASC
/
