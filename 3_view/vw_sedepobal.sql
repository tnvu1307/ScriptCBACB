SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW VW_SEDEPOBAL
(AUTOID, ACCTNO, FRDATE, DAYS, QTTY, 
 DELTD, ID, AMT, TODATE, FEERATE, 
 FORP)
AS 
SELECT SED.AUTOID,SED.ACCTNO,SED.TXDATE FRDATE,SED.DAYS,SED.QTTY,SED.DELTD,SED.ID,SED.AMT,SED.TXDATE + SED.DAYS - 1 TODATE,
        CIF.FEEAMT FEERATE,CIF.FORP
FROM SEDEPOBAL SED,SEMAST SE,CIFEEDEF CIF, DDMAST CI
WHERE SED.ACCTNO=SE.ACCTNO
    AND SE.AFACCTNO = CI.AFACCTNO
    AND CI.ACTYPE = CIF.ACTYPE
    AND FEETYPE = 'VSDDEP'
    AND CIF.SECTYPE = (SELECT CASE WHEN SB.SECTYPE='001' OR SB.SECTYPE='002' OR SB.SECTYPE='007' OR SB.SECTYPE='008' THEN '111'
                                   WHEN SB.SECTYPE='003' OR SB.SECTYPE='006' THEN '222' ELSE SB.SECTYPE END SECTYPE
                       FROM SBSECURITIES SB WHERE SE.CODEID = SB.CODEID)
/
