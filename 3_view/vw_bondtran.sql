SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW VW_BONDTRAN
(TXNUM, TXDATE, ACCTNO, TXCD, NAMT, 
 CAMT, REF, DELTD, AUTOID, ACCTREF, 
 TLTXCD, BKDATE, TRDESC)
AS 
select "TXNUM","TXDATE","ACCTNO","TXCD","NAMT","CAMT","REF","DELTD","AUTOID","ACCTREF","TLTXCD","BKDATE","TRDESC"
    from BONDSETRAN
  union all
  select "TXNUM","TXDATE","ACCTNO","TXCD","NAMT","CAMT","REF","DELTD","AUTOID","ACCTREF","TLTXCD","BKDATE","TRDESC"
    from BONDSETRANA
/
