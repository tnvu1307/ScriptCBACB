SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW V_RM_GETTRFQUEUE
(AUTOID, VERSION, TXDATE, AFFECTDATE, BANKCODE, 
 TRFCODE, CORD, UNHOLD, REFACCTNO, REFACCTNAME)
AS 
SELECT CRT.AUTOID,CRT.VERSION,CRT.TXDATE,CRT.AFFECTDATE,CRT.REFBANK BANKCODE,
CRT.TRFCODE,CRA.REFDORC CORD,CRA.REFUNHOLD UNHOLD,CRA.REFACCTNO,CRA.REFACCTNAME
FROM CRBTRFLOG CRT,CRBDEFACCT CRA,CRBDEFBANK CRB
WHERE CRT.REFBANK=CRA.REFBANK AND CRT.TRFCODE=CRA.TRFCODE
AND CSPKS_RMPROC.is_number(CRA.MSGID)=1 AND CRT.STATUS='A'
AND CRT.REFBANK=CRB.BANKCODE AND CRB.STATUS='A'
ORDER BY CRT.VERSION,CRT.REFBANK
/
