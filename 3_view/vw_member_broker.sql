CREATE OR REPLACE FORCE VIEW VW_MEMBER_BROKER
(FILTERCD, VALUE, VALUECD, DISPLAY, EN_DISPLAY, 
 DESCRIPTION)
AS 
SELECT M.AUTOID FILTERCD, N.AUTOID VALUE, N.AUTOID VALUECD, N.EXTRAVAL  DISPLAY,
N.EXTRAVAL  EN_DISPLAY, N.EXTRAVAL DESCRIPTION
from FAMEMBERS M, (select * from FAMEMBERSEXTRA where  Extracd = 'B' and status = 'A')N --Trang thai 111000 Active
where  M.AUTOID = N.MEMBERID
order by description
/
