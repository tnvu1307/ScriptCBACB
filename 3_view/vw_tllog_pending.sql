SET DEFINE OFF;
CREATE OR REPLACE FORCE VIEW VW_TLLOG_PENDING
(TLTXCD, TXNUM, TXDATE, TXSTATUS, MSGACCT, 
 CFCUSTODYCD, FLDCD, NVALUE, CVALUE)
AS 
SELECT TL.TLTXCD, TL.TXNUM, TL.TXDATE, TL.TXSTATUS, TL.MSGACCT, TL.CFCUSTODYCD, TF.FLDCD, TF.NVALUE, TF.CVALUE
FROM TLLOG TL, TLLOGFLD TF 
WHERE TL.TXNUM = TF.TXNUM AND TL.TXDATE = TF.TXDATE AND DELTD <> 'Y' AND TXSTATUS='4'
/
