FUNCTION fn_txPreAppCheck(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
  V_COUNT     NUMBER;
    V_CURRDATE      DATE;
BEGIN
   plog.setbeginsection (pkgctx, 'fn_txPreAppCheck');
   plog.debug(pkgctx,'BEGIN OF fn_txPreAppCheck');
   /***************************************************************************************************
    * PUT YOUR SPECIFIC RULE HERE, FOR EXAMPLE:
    * IF NOT <<YOUR BIZ CONDITION>> THEN
    *    p_err_code := '<<ERRNUM>>'; -- Pre-defined in DEFERROR table
    *    plog.setendsection (pkgctx, 'fn_txPreAppCheck');
    *    RETURN errnums.C_BIZ_RULE_INVALID;
    * END IF;
    ***************************************************************************************************/
     V_CURRDATE := getcurrdate;
    IF p_txmsg.deltd <> 'Y' THEN -- Normal transaction
        select count(1) CNT INTO V_COUNT from SEDEPOSIT
        where autoid = p_txmsg.txfields('05').value
            and SENDDEPODATE >  V_CURRDATE;
        IF V_COUNT > 0 THEN
            p_err_code := '-900011';
            plog.setendsection (pkgctx, 'fn_txAppAutoCheck');
            RETURN errnums.C_BIZ_RULE_INVALID;
        END IF;


        begin
            select count(1) into V_COUNT
            from vsdtxreq
            where objname = '2241' and refcode = TO_CHAR(p_txmsg.txfields('05').value) and msgstatus in ('P','S','A');
        EXCEPTION
        WHEN OTHERS
        THEN
            V_COUNT := 0;
        END;

        IF V_COUNT > 0 THEN
            p_err_code := '-100122';
            plog.setendsection (pkgctx, 'fn_txAppAutoCheck');
            RETURN errnums.C_BIZ_RULE_INVALID;
        END IF;
    ELSE
        SELECT COUNT(*) INTO V_COUNT
        FROM SEMAST
        WHERE TRIM(ACCTNO)=p_txmsg.txfields('03').value AND DEPOSIT>= TO_NUMBER(p_txmsg.txfields('10').value);

        IF V_COUNT = 0 THEN
            p_err_code := '-900033';
            plog.setendsection (pkgctx, 'fn_txAppAutoCheck');
            RETURN errnums.C_BIZ_RULE_INVALID;
        END IF;
    END IF;
    plog.debug (pkgctx, '<<END OF fn_txPreAppCheck');
    plog.setendsection (pkgctx, 'fn_txPreAppCheck');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
      plog.setendsection (pkgctx, 'fn_txPreAppCheck');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txPreAppCheck;

