FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    V_ENDMONTH DATE;
    V_CVALDATE VARCHAR2(20);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    IF P_TXMSG.DELTD <> 'Y' THEN
        V_CVALDATE := P_TXMSG.TXFIELDS(C_VALDATE).VALUE;

        SELECT MAX(SB.SBDATE) INTO V_ENDMONTH
        FROM SBCLDR SB
        WHERE SB.CLDRTYPE = '000'
        AND SB.HOLIDAY = 'N'
        AND TO_CHAR(SB.SBDATE,'MMRRRR') = V_CVALDATE;

        IF V_ENDMONTH IS NOT NULL THEN
            CSPKS_FEECALCNEW.PRC_SEDEPO(V_ENDMONTH, P_TXMSG.TXNUM, P_ERR_CODE);
            CSPKS_FEECALCNEW.PRC_ORDER(V_ENDMONTH, P_TXMSG.TXNUM, P_ERR_CODE);
            CSPKS_FEECALCNEW.PRC_MINIMUM(V_ENDMONTH, P_TXMSG.TXNUM, P_ERR_CODE);
            CSPKS_FEECALCNEW.PRC_FEEBOOKINGRESULTAUTO(V_ENDMONTH, P_TXMSG.TXNUM, P_ERR_CODE);
        END IF;

    END IF;
    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
