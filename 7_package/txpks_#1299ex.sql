FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
l_feecode varchar2(100);
l_feeamount number;
l_taxamount number;
l_profoliocd varchar2(50);
l_ccycd varchar2(10);
l_autoid varchar(100);
l_bankacctno varchar2(50);
l_desc varchar2(250);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    l_feecode:=p_txmsg.txfields('08').value;
    l_profoliocd:=p_txmsg.txfields('91').value;
    l_ccycd:=p_txmsg.txfields('21').value;
    l_autoid:=p_txmsg.txfields('22').value;
    l_bankacctno:=p_txmsg.txfields('96').value;

    
    IF p_txmsg.deltd <> 'Y' THEN
        select taxamount,feeamount,remark into l_taxamount,l_feeamount,l_desc from fee_booking_result
        where  status ='P' and feecode=l_feecode and cifid=l_profoliocd  and autoid=l_autoid AND DELTD <>'Y';--and bankaccount=l_bankacctno trung.luu: 19-04-2021 Khong bat buoc co tai khoan tien
        if l_feeamount <> p_txmsg.txfields('10').value or l_taxamount <> p_txmsg.txfields('09').value or l_desc <> p_txmsg.txfields('30').value then
            UPDATE fee_booking_result
            SET feeamount=p_txmsg.txfields('10').value,
                taxamount=p_txmsg.txfields('09').value,
                remark = p_txmsg.txfields('30').value
            WHERE  status ='P' and feecode=l_feecode and cifid=l_profoliocd AND CURRENCY =l_ccycd  and autoid=l_autoid;--and bankaccount=l_bankacctno trung.luu: 19-04-2021 Khong bat buoc co tai khoan tien
        end if;
    END IF;
    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

