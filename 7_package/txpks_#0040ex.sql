       SELECT  ACTIVE
      INTO l_ACTIVE
        FROM TLPROFILES
        WHERE TLID = p_txmsg.txfields('09').value;

     IF NOT ( INSTR('Y',l_ACTIVE) > 0) THEN
        p_err_code := '-200085';
        RETURN errnums.C_BIZ_RULE_INVALID;
     END IF;

