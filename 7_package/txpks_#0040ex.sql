      INTO l_count
        FROM TLGRPUSERS
        WHERE TLID = p_txmsg.txfields('09').value
              AND GRPID = p_txmsg.txfields('10').value;

     IF l_count = 0 THEN
        p_err_code := '-200086';
        RETURN errnums.C_BIZ_RULE_INVALID;
        END IF;
    END IF;
