FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    
    IF p_txmsg.deltd <> 'Y' THEN -- Normal transaction
        BEGIN

            PCK_BANKAPI.se_unhold(
                          p_txmsg.txfields('03').value, -- tk semast
                          p_txmsg.txfields('05').value,
                          '',
                          '',
                          p_txmsg.txfields('10').value,  --- so luong
                          p_txmsg.txfields('30').value, -- dien giai
                          p_txmsg.tlid, -- nguoi lap giao dich
                          P_ERR_CODE  );
            if P_ERR_CODE <> systemnums.C_SUCCESS then
                p_err_code := '-900034';
                plog.setendsection (pkgctx, 'fn_txAppAutoCheck');
                RETURN errnums.C_BIZ_RULE_INVALID;
            else
                update buf_se_member
                    set     unhold_no_broker = totalhold - totalunhold
                where   totalhold - totalunhold <> 0
                    and custodycd  = p_txmsg.txfields('88').value
                    and symbol = p_txmsg.txfields('14').value
                ;
            end if;
            EXCEPTION WHEN OTHERS THEN
                
                plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
                RETURN errnums.C_BIZ_RULE_INVALID;
        END;
    end if;
    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

