                update odmastvsd  set deltd = 'Y' where autoid = p_txmsg.txfields('01').value;
            end if;
        else
            
            if p_txmsg.txfields('06').value = 'Client' then
                update odmastcust set deltd = 'Y' where fileid = p_txmsg.txfields('28').value;
            end if;
            if p_txmsg.txfields('06').value = 'Broker' then
                update odmastcmp  set deltd = 'Y' where fileid = p_txmsg.txfields('28').value;
            end if;
