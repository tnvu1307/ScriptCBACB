    IF p_txmsg.deltd <> 'Y' THEN -- Normal transaction
        if p_txmsg.txfields('32').value = 'Y' then -- = 'Y' => xoa theo tung lenh (autoid)
            
            if p_txmsg.txfields('06').value = 'Client' then
                update odmastcust set deltd = 'Y' where autoid = p_txmsg.txfields('01').value;
