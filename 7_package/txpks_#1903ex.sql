FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
v_acctno VARCHAR2(30);
v_afacctno VARCHAR2(20);
v_bondcode VARCHAR2(30);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    IF p_txmsg.deltd <> 'Y' THEN
        SELECT max(bo.afacctno) INTO v_afacctno FROM BONDSEMAST bo where  bo.custodycd = p_txmsg.txfields(c_custodycd).value;
        v_bondcode := p_txmsg.txfields(c_bondcode).value;
        v_acctno   := v_afacctno || v_bondcode;
        --
        UPDATE BONDSEMAST SET TRADE = TRADE - p_txmsg.txfields(c_quantity).value WHERE ACCTNO = v_acctno;
        INSERT INTO BONDSETRAN (TXNUM, TXDATE, ACCTNO, TXCD, NAMT, CAMT, REF, DELTD, AUTOID, ACCTREF, TLTXCD, BKDATE, TRDESC)
            VALUES (p_txmsg.txnum, TO_DATE(p_txmsg.txdate, systemnums.C_DATE_FORMAT), v_acctno, '1903', p_txmsg.txfields(c_quantity).value
                   , null, v_acctno, 'N', SEQ_BONDSETRAN.NEXTVAL, v_acctno, p_txmsg.tltxcd, TO_DATE(p_txmsg.busdate, systemnums.C_DATE_FORMAT) , p_txmsg.txfields(c_desc).value);
    END IF;
    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

