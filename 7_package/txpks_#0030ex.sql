FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    V_DIRECTSYNC VARCHAR2(3);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/

    -- gen buffer ci
    plog.debug (pkgctx, 'afacctno: ' || p_txmsg.txfields('03').value);
    BEGIN
        SELECT VARVALUE INTO V_DIRECTSYNC FROM SYSVAR WHERE VARNAME='DIRECTSYNC0030' AND ROWNUM <=1;

        EXCEPTION
        WHEN OTHERS
           THEN
           V_DIRECTSYNC := 'N';
    END;
    BEGIN
        if V_DIRECTSYNC = 'Y' then
            jbpks_auto.pr_gen_buf_dd_account(p_txmsg.txfields('03').value);
        else
            jbpks_auto.pr_trg_account_log(p_txmsg.txfields('03').value,'CI');
        end if;
        --COMMIT;
    END;
    -- Gen buffer od
    FOR rec IN
        (
            SELECT od.orderid FROM odmast od WHERE od.afacctno = p_txmsg.txfields('03').value
            UNION ALL
            SELECT fo.acctno orderid FROM fomast fo WHERE fo.afacctno = p_txmsg.txfields('03').value
        )
    LOOP
        BEGIN
            plog.debug (pkgctx, 'rec.orderid: ' || rec.orderid);

            if V_DIRECTSYNC = 'Y' then
                jbpks_auto.pr_gen_buf_od_account(rec.orderid);
            else
                jbpks_auto.pr_trg_account_log(rec.orderid,'OD');
            end if;
            --COMMIT;
        END;
    END LOOP;

    /*-- Gen buffer se
    FOR rec IN
        (
            SELECT se.acctno FROM semast se WHERE se.afacctno = p_txmsg.txfields('03').value
        )
    LOOP
        BEGIN
            plog.debug (pkgctx, 'rec.acctno: ' || rec.acctno);

            if V_DIRECTSYNC = 'Y' then
                jbpks_auto.pr_gen_buf_se_account(rec.acctno);
            else
                jbpks_auto.pr_trg_account_log(rec.acctno,'SE');
            end if;

            --COMMIT;
        END;
    END LOOP;*/

    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
