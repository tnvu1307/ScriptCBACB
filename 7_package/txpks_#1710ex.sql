    *    plog.setendsection (pkgctx, 'fn_txAftAppCheck');
    *    RETURN errnums.C_BIZ_RULE_INVALID;
    * END IF;
    ***************************************************************************************************/
    IF p_txmsg.deltd <> 'Y' THEN -- Reversal transaction
        select nvl(max(cfnstatus),'x') into v_cfnstatus
        from swiftmsgmaplog
        where msgid = p_txmsg.txfields('01').value ;
        IF NOT v_cfnstatus='P' THEN
            p_err_code := '-107701'; -- Pre-defined in DEFERROR table
            plog.setendsection (pkgctx, 'fn_txAftAppCheck');
            RETURN errnums.C_BIZ_RULE_INVALID;
        END IF;

        --SHBVNEX-1867
        SELECT COUNT(1) INTO V_COUNT
        FROM SWIFTMSGMAPLOG
        WHERE MSGID = P_TXMSG.TXFIELDS('01').VALUE
        AND MTTYPE = '380';

        IF V_COUNT > 0 AND P_TXMSG.TXFIELDS('10').VALUE = 'C' THEN
            P_ERR_CODE := '-107702';
            PLOG.SETENDSECTION (PKGCTX, 'FN_TXAFTAPPCHECK');
            RETURN ERRNUMS.C_BIZ_RULE_INVALID;
        END IF;
    END IF;


   plog.debug (pkgctx, '<<END OF fn_txAftAppCheck>>');
   plog.setendsection (pkgctx, 'fn_txAftAppCheck');
   RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
      plog.setendsection (pkgctx, 'fn_txAftAppCheck');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppCheck;

