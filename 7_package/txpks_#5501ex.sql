FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    p_data_source varchar2(4000);
    v_desc  varchar2(4000);
    v_email varchar2(4000);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/

    IF p_txmsg.deltd <> 'Y' THEN
        --log req
        INSERT INTO ONLREQUESTLOG (AUTOID,TLTXCD,CUSTODYCD,CODEID,QTTY,USER_CREATED,TIME_CREATED,USER_APPR,TIME_APPR,TXDATE,TXNUM,STATUS,DESCRIPTION)
        VALUES(seq_ONLREQUESTLOG.NEXTVAL,p_txmsg.tltxcd,p_txmsg.txfields(c_custodycd).value,'',0,p_txmsg.txfields(c_tlid).value,SYSDATE,NULL,NULL,p_txmsg.TXDATE,p_txmsg.TXNUM,'P',p_txmsg.txfields(c_description).value);


        --trung.luu: 27-01-2021 --SHBVNEX-1684
        begin
            select emailcs into v_email from templates where code= '268E';
            select en_txdesc into v_desc from tltx where tltxcd = p_txmsg.tltxcd;
        exception when NO_DATA_FOUND
            then
            v_email:= '';
            v_desc:= '';
        end;
        p_data_source :=  'select  ''' ||p_txmsg.txfields(c_custodycd).value|| '''p_custodycd,''' ||v_desc|| ''' p_txdesc from dual';
        nmpks_ems.InsertEmailLog(v_email, '268E' ,p_data_source ,p_txmsg.tlid);
    end if;

    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

