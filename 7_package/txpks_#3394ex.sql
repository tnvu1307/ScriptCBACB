        STATUS = SUBSTR(PSTATUS,LENGTH(PSTATUS),1),
        LAST_CHANGE = SYSTIMESTAMP
        WHERE CAMASTID = P_TXMSG.TXFIELDS('03').VALUE;

        UPDATE CASCHD
        SET PSTATUS=PSTATUS||STATUS,
        STATUS = SUBSTR(PSTATUS,LENGTH(PSTATUS),1)
        WHERE CAMASTID = P_TXMSG.TXFIELDS('03').VALUE
