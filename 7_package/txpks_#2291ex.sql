FUNCTION fn_txPreAppCheck(p_txmsg in out tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    l_count number;
BEGIN
   plog.setbeginsection (pkgctx, 'fn_txPreAppCheck');
   plog.debug(pkgctx,'BEGIN OF fn_txPreAppCheck');
   /***************************************************************************************************
    * PUT YOUR SPECIFIC RULE HERE, FOR EXAMPLE:
    * IF NOT <<YOUR BIZ CONDITION>> THEN
    *    p_err_code := '<<ERRNUM>>'; -- Pre-defined in DEFERROR table
    *    plog.setendsection (pkgctx, 'fn_txPreAppCheck');
    *    RETURN errnums.C_BIZ_RULE_INVALID;
    * END IF;
    ***************************************************************************************************/
    select count(1) into l_count
    from
    (select actype from afmast af, cfmast cf
        where cf.custid = af.custid
        and cf.custodycd = p_txmsg.txfields(c_custodycd).value and af.status <> 'C'
        group by actype having count(1) > 1);
    if l_count > 0 then
        p_txmsg.txWarningException('-1001401').value:= cspks_system.fn_get_errmsg('-100140');
        p_txmsg.txWarningException('-1001401').errlev:= '1';
    end if;
    select count(1) into l_count
    from
    (select af.bankname, af.bankacctno from afmast af, cfmast cf
        where cf.custid = af.custid
        and cf.custodycd = p_txmsg.txfields(c_custodycd).value and af.status <> 'C' and af.bankname is not null and af.bankacctno is not null
        group by af.bankname, af.bankacctno having count(1) > 1);
    if l_count > 0 then
        p_txmsg.txWarningException('-1001471').value:= cspks_system.fn_get_errmsg('-100147');
        p_txmsg.txWarningException('-1001471').errlev:= '1';
    end if;
    plog.debug (pkgctx, '<<END OF fn_txPreAppCheck');
    plog.setendsection (pkgctx, 'fn_txPreAppCheck');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
      plog.setendsection (pkgctx, 'fn_txPreAppCheck');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txPreAppCheck;

