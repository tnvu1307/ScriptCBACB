FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   accMoney := p_txmsg.txfields('36').value;
    moneyNumber := p_txmsg.txfields('13').value;
   IF p_txmsg.deltd <> 'Y' THEN    
       update MONEYACCOUNT set BLOCKBALANCE = BLOCKBALANCE - moneyNumber where ACCOUNTNUMBER  = accMoney;
        INSERT INTO BLOCKBALANCEHISTORY (AUTOID, TLTXCD,TXNUM, TXDATE, TXTYPE, AMT, NOTE,ACCOUNTNUMBER)
        values(SEQ_AUTOID.NEXTVAL,'0032',p_txmsg.txnum ,p_txmsg.txdate, 'D', moneyNumber,p_txmsg.txfields('30').value,accMoney);
    ELSE
        update MONEYACCOUNT set BLOCKBALANCE = BLOCKBALANCE + moneyNumber where ACCOUNTNUMBER  = accMoney;
        update BLOCKBALANCEHISTORY set deltd = 'Y' where TXNUM = p_txmsg.txnum and TXDATE = p_txmsg.txdate;
    END IF;
    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
