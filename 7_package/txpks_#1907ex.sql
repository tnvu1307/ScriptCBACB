FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
  v_bondactype varchar2(10);
  v_symbol varchar2(10);
  v_valuatoindate date;
  v_newprice number;
  v_pricetype varchar2(10);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    v_bondactype := p_txmsg.txfields(c_bondactype).value;
    v_symbol := p_txmsg.txfields(c_symbol).value;
    v_valuatoindate := to_date(p_txmsg.txfields(c_valuatoindate).value,systemnums.C_DATE_FORMAT);
    v_newprice := to_number(p_txmsg.txfields(c_newprice).value);
    v_pricetype := p_txmsg.txfields(c_valmethod).value;
    -------------------TRIBUI--------------------
    v_pricetype := SUBSTR(v_pricetype,INSTR(v_pricetype,'|',1)+1,3);
    ---------------------------------------------	    
    IF p_txmsg.deltd <> 'Y' THEN -- NORMAL TRANSACTION
      UPDATE BONDINFO bo SET bo.bondprice = v_newprice, bo.pricetype = v_pricetype
             WHERE BONDACTYPE = v_bondactype AND TXDATE = v_valuatoindate AND SYMBOL = v_symbol;
    END IF;
    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
