FUNCTION fn_txAftAppUpdate(p_txmsg in tx.msg_rectype,p_err_code out varchar2)
RETURN NUMBER
IS
    L_COUNT NUMBER;
    L_REQTXNUM VARCHAR2(100);
    l_dorc varchar2(20);
BEGIN
    plog.setbeginsection (pkgctx, 'fn_txAftAppUpdate');
    plog.debug (pkgctx, '<<BEGIN OF fn_txAftAppUpdate');
   /***************************************************************************************************
    ** PUT YOUR SPECIFIC AFTER PROCESS HERE. DO NOT COMMIT/ROLLBACK HERE, THE SYSTEM WILL DO IT
    ***************************************************************************************************/
    IF p_txmsg.deltd <> 'Y' THEN -- Normal transaction
        L_REQTXNUM := P_TXMSG.TXFIELDS ('95').VALUE;
        l_dorc := p_txmsg.txfields ('91').VALUE;

        IF l_dorc =  'C' THEN
            INSERT INTO DDTRAN(TXNUM,TXDATE,ACCTNO,TXCD,NAMT,CAMT,ACCTREF,DELTD,REF,AUTOID,TLTXCD,BKDATE,TRDESC)
            VALUES (p_txmsg.txnum, TO_DATE (p_txmsg.txdate, systemnums.C_DATE_FORMAT),p_txmsg.txfields ('06').value,'0002',ROUND(p_txmsg.txfields('10').value,0),NULL,'',p_txmsg.deltd,'',seq_DDTRAN.NEXTVAL,p_txmsg.tltxcd,p_txmsg.busdate,'' || '' || '');

            UPDATE DDMAST
            SET
            BALANCE = BALANCE + (ROUND(p_txmsg.txfields('10').value,0)), LAST_CHANGE = SYSTIMESTAMP
            WHERE ACCTNO=p_txmsg.txfields('06').value;
        END IF;

        IF l_dorc =  'D' THEN
            INSERT INTO DDTRAN(TXNUM,TXDATE,ACCTNO,TXCD,NAMT,CAMT,ACCTREF,DELTD,REF,AUTOID,TLTXCD,BKDATE,TRDESC)
            VALUES (p_txmsg.txnum, TO_DATE (p_txmsg.txdate, systemnums.C_DATE_FORMAT),p_txmsg.txfields ('06').value,'0010',ROUND(p_txmsg.txfields('10').value,0),NULL,'',p_txmsg.deltd,'',seq_DDTRAN.NEXTVAL,p_txmsg.tltxcd,p_txmsg.busdate,'' || '' || '');

            UPDATE DDMAST
            SET
            NETTING = NETTING - (ROUND(p_txmsg.txfields('10').value,0)),
            LAST_CHANGE = SYSTIMESTAMP
            WHERE ACCTNO=p_txmsg.txfields('06').value;

            IF L_REQTXNUM LIKE 'VSTP1.%' THEN
                FOR REC IN (
                    SELECT * FROM VSTP_SETTLE_LOG WHERE TO_CHAR(AUTOID) = SUBSTR(L_REQTXNUM, INSTR(L_REQTXNUM, '.') + 1)
                ) LOOP
                    UPDATE VSTP_SETTLE_LOG
                    SET STATUS = '2.1', PSTATUS = STATUS, LASTCHANGE = SYSTIMESTAMP
                    WHERE AUTOID = REC.AUTOID;

                    UPDATE ODMAST
                    SET ISPAYMENT = 'Y', LASTCHANGE = SYSTIMESTAMP
                    WHERE ORDERID = REC.ORDERID;

                    UPDATE STSCHD
                    SET STATUS = 'C', LASTCHANGE = SYSTIMESTAMP
                    WHERE ORDERID = REC.ORDERID
                    AND DUETYPE = 'SM';
                END LOOP;
            END IF;
        end if;
    END IF;


    plog.debug (pkgctx, '<<END OF fn_txAftAppUpdate');
    plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
    RETURN systemnums.C_SUCCESS;
EXCEPTION
WHEN OTHERS
   THEN
      p_err_code := errnums.C_SYSTEM_ERROR;
      plog.error (pkgctx, SQLERRM || dbms_utility.format_error_backtrace);
       plog.setendsection (pkgctx, 'fn_txAftAppUpdate');
      RAISE errnums.E_SYSTEM_ERROR;
END fn_txAftAppUpdate;

